/**
 * USER_INFO 회원정보
 */
DROP TABLE USER_INFO;

CREATE TABLE `USER_INFO`
(
	`EMAIL`      VARCHAR(100) COLLATE UTF8MB4_UNICODE_CI NOT NULL COMMENT '이메일',
	`PWD`        VARCHAR(500) COLLATE UTF8MB4_UNICODE_CI NOT NULL COMMENT '비밀번호',
	`NAME`       VARCHAR(100) COLLATE UTF8MB4_UNICODE_CI NOT NULL COMMENT '이름',
	`PHONE`      VARCHAR(100) COLLATE UTF8MB4_UNICODE_CI NOT NULL COMMENT '연락처',
	`SMS_YN`     VARCHAR(1) COLLATE UTF8MB4_UNICODE_CI   NOT NULL DEFAULT 'N' COMMENT 'SMS수신동의여부',
	`EMAIL_YN`   VARCHAR(1) COLLATE UTF8MB4_UNICODE_CI   NOT NULL DEFAULT 'N' COMMENT 'EMAIL수신동의여부',
	`OAUTH_TYPE` VARCHAR(1) COLLATE UTF8MB4_UNICODE_CI COMMENT '소셜로그인구분(G:GOOGLE,N:NAVER,K:KAKAO)',
	`OAUTH_KEY`  VARCHAR(100) COLLATE UTF8MB4_UNICODE_CI COMMENT '소셜로그인KEY',
	`INPUT_DT`   TIMESTAMP                               NOT NULL COMMENT '입력일',
	`INPUT_ID`   VARCHAR(100) COLLATE UTF8MB4_UNICODE_CI NOT NULL COMMENT '입력자',
	`UPDATE_DT`  TIMESTAMP                               NOT NULL COMMENT '수정일',
	`UPDATE_ID`  VARCHAR(100) COLLATE UTF8MB4_UNICODE_CI NOT NULL COMMENT '수정자',
	PRIMARY KEY (`EMAIL`)) ENGINE = InnoDB
						   AUTO_INCREMENT = 8
						   DEFAULT CHARSET = UTF8MB4
						   COLLATE = UTF8MB4_UNICODE_CI COMMENT ='사용자정보'
;

