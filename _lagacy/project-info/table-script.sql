/**
 * EDT_MSG_INFO EDITOR MESSAGE 저장
 */
DROP TABLE EDT_MSG_INFO;

CREATE TABLE EDT_MSG_INFO
(
	SEQ           INT AUTO_INCREMENT COMMENT 'SEQ' PRIMARY KEY,
	EDT_ID        VARCHAR(100)                                NOT NULL COMMENT 'EDITOR 아이디',
	EDT_TITLE     VARCHAR(4000)                               NOT NULL COMMENT '제목',
	EDT_SUB_TITLE VARCHAR(4000) DEFAULT '소제목'                 NULL,
	EDT_TEXT      VARCHAR(4000)                               NOT NULL COMMENT '내용',
	MAIN_IMG_PATH VARCHAR(400)  DEFAULT '대표이미지경로'             NULL,
	VIEW_CNT      INT           DEFAULT 0                     NULL COMMENT '조회수',
	PAGE          VARCHAR(200)                                NOT NULL COMMENT '페이지',
	QNA_PASSWORD  VARCHAR(100)                                NULL COMMENT '비밀글 비밀번호',
	QNA_SECURE_YN VARCHAR(1)    DEFAULT 'N'                   NULL COMMENT '비밀글 여부',
	DEL_YN        VARCHAR(1)    DEFAULT 'N'                   NULL COMMENT '삭제여부',
	INPUT_ID      VARCHAR(100)                                NOT NULL COMMENT '입력자',
	INPUT_NAME    VARCHAR(100)                                NULL COMMENT '등록자 이름',
	INPUT_DT      TIMESTAMP                                   NOT NULL COMMENT '입력일',
	UPDATE_ID     VARCHAR(100)                                NOT NULL COMMENT '수정자',
	UPDATE_NAME   VARCHAR(100)                                NULL COMMENT '수정자 이름',
	UPDATE_DT     TIMESTAMP                                   NOT NULL COMMENT '수정일')
	COMMENT '게시판메시지' COLLATE = UTF8MB4_UNICODE_CI;


