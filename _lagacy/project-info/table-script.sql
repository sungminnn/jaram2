/**
 * 파일 저장
 */
DROP TABLE FILE_INFO;
CREATE TABLE `FILE_INFO`
(
	`SEQ`            INT COLLATE UTF8MB4_UNICODE_CI AUTO_INCREMENT NOT NULL COMMENT '파일번호(PK)',
	`EDT_SEQ`        BIGINT(20)                                    NOT NULL COLLATE UTF8MB4_UNICODE_CI COMMENT '게시글 번호 (FK)',
	`ORG_FILE_NAME`  VARCHAR(255)                                  NOT NULL COLLATE UTF8MB4_UNICODE_CI COMMENT '원본 파일명',
	`SAVE_FILE_NAME` VARCHAR(40)                                   NOT NULL COLLATE UTF8MB4_UNICODE_CI COMMENT '저장 파일명',
	`FILE_SIZE`      INT(11)                                       NOT NULL COLLATE UTF8MB4_UNICODE_CI COMMENT '파일 크기',
	`INPUT_DT`       TIMESTAMP                                     NOT NULL COMMENT '생성일시',
	`INPUT_ID`       VARCHAR(100) COLLATE UTF8MB4_UNICODE_CI       NOT NULL COMMENT '입력자',
	PRIMARY KEY (`SEQ`)) COMMENT '파일 저장';

