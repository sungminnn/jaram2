-- =========================================================
-- JARAM SUPABASE DATABASE SQL
-- schema: jaram
-- description:
--   - 자람 사이트용 기본 테이블/권한/RLS 정의
--   - 현재 posts 테이블만 RLS 적용
--   - legacy_users 는 마이그레이션 보관용
--   - file_info 는 첨부파일 메타 정보용
