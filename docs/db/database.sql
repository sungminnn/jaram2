-- =========================================================
-- 1. SCHEMA
-- =========================================================

create schema if not exists jaram;


-- =========================================================
-- 2. TABLES
-- =========================================================

-- -----------------------------------------
-- 2.1 posts
-- 설명:
--   - 공지사항, 뉴스, 갤러리, qna 등 게시글 공통 테이블
--   - legacy EDT_MSG_INFO 이관 대상
-- -----------------------------------------
create table if not exists jaram.posts (
  id bigint generated by default as identity primary key,

  title text not null,
  sub_title text,
  content text not null,

  main_image_url text,
  view_count integer default 0,

  page text not null,

  is_private boolean default false,
  password text,

  is_deleted boolean default false,

  author_id text,
  author_name text,

  created_at timestamptz default now(),
  updated_at timestamptz default now()
);


comment on table jaram.posts is '게시글 공통 테이블';
comment on column jaram.posts.title is '제목';
comment on column jaram.posts.sub_title is '소제목';
comment on column jaram.posts.content is '본문 HTML';
comment on column jaram.posts.main_image_url is '대표 이미지 URL';
comment on column jaram.posts.view_count is '조회수';
comment on column jaram.posts.page is '게시판 구분(notice, news, gallery, qna, ceo 등)';
comment on column jaram.posts.is_private is '비밀글 여부';
comment on column jaram.posts.password is '비밀글 비밀번호 또는 해시';
comment on column jaram.posts.is_deleted is '삭제 여부';
comment on column jaram.posts.author_id is '작성자 ID 또는 이메일';
comment on column jaram.posts.author_name is '작성자 이름';
comment on column jaram.posts.created_at is '생성일시';
comment on column jaram.posts.updated_at is '수정일시';


-- -----------------------------------------
-- 2.2 file_info
-- 설명:
--   - 게시글/댓글 첨부파일 메타 정보
--   - 실제 파일은 추후 Supabase Storage 사용 예정
-- -----------------------------------------
create table if not exists jaram.file_info (
  id bigint generated by default as identity primary key,

  post_id bigint,
  comment_id bigint,

  original_file_name text not null,
  stored_file_name text not null,

  file_size bigint not null,

  created_at timestamptz default now(),
  created_by text
);


comment on table jaram.file_info is '첨부파일 메타 정보';
comment on column jaram.file_info.post_id is '게시글 ID';
comment on column jaram.file_info.comment_id is '댓글 ID';
comment on column jaram.file_info.original_file_name is '원본 파일명';
comment on column jaram.file_info.stored_file_name is '저장 파일명';
comment on column jaram.file_info.file_size is '파일 크기(byte)';
comment on column jaram.file_info.created_at is '생성일시';
comment on column jaram.file_info.created_by is '등록자';

-- Storage 경로 규칙:
-- - stored_file_name 에 폴더가 없으면 created_at 기준 uploadfile/YYMMDD 폴더를 붙인다.
-- - 예: created_at = 2026-04-23, stored_file_name = abc.pdf -> attachment/uploadfile/260423/abc.pdf
-- - stored_file_name 에 날짜 폴더가 포함되어 있으면 uploadfile 을 앞에 붙여 사용한다.
-- - stored_file_name 이 이미 uploadfile/ 로 시작하면 해당 경로를 그대로 사용한다.



-- -----------------------------------------
-- 2.3 legacy_users
